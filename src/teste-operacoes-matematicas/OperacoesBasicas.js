const soma = (a , b) => {
    return Number(a) + Number(b)
}

const subtracao = (a , b) => {
    return Number(a) - Number(b)
}

const multiplicacao = (a , b) => {
    return Number(a) * Number(b)
}

const divisao = (a , b) => {
    return Number(a) / Number(b)
}

export { soma, subtracao, multiplicacao, divisao }